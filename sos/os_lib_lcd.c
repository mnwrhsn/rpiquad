//current position
int lcd_n5110_index = 0;

void lcd_n5110_init(void)
{
	LCD_N5110_PORT |= (1 << LCD_N5110_RST);	//pull up rst

	LCD_N5110_DDR |= ((1 << LCD_N5110_CE) | (1 << LCD_N5110_RST) |
			(1 << LCD_N5110_DC) | (1 << LCD_N5110_DIN) |
			(1 << LCD_N5110_CLK));
	_delay_ms(15);

	LCD_N5110_PORT &= ~(1 << LCD_N5110_RST);
	_delay_ms(64);
	LCD_N5110_PORT |= (1 << LCD_N5110_RST);
	LCD_N5110_PORT &= ~(1 << LCD_N5110_CE);	//disable controller

	lcd_n5110_send(0x21, LCD_N5110_CMD);	//lcd extended cmd
	lcd_n5110_send(0xc8, LCD_N5110_CMD);	//set contrast
	lcd_n5110_send(0x06, LCD_N5110_CMD);	//temp coeff
	lcd_n5110_send(0x13, LCD_N5110_CMD);	//bias mode 1:48
	lcd_n5110_send(0x20, LCD_N5110_CMD);	//std mode, horizontal addr
	lcd_n5110_send(0x0c, LCD_N5110_CMD);	//normal mode

	lcd_n5110_clear();

	lcd_n5110_send(0x21, LCD_N5110_CMD);		//ext cmd
	lcd_n5110_send((0x80 | 0x40), LCD_N5110_CMD);	//contrast
	lcd_n5110_send(0x20, LCD_N5110_CMD);	//std cmd, horiz addrssing
}

void lcd_n5110_clear(void)
{
	int cnt;
	int lcd_sz = ((LCD_N5110_WIDTH * LCD_N5110_HEIGHT) / LCD_N5110_CHEIGT);
	lcd_n5110_index = 0;
	lcd_n5110_base_addr(0);
	for (cnt = 0; cnt < lcd_sz; cnt++)
		lcd_n5110_send(0, LCD_N5110_DATA);
}

void lcd_n5110_clear_area(uint8_t line, uint8_t start, uint8_t end)
{
	int cnt;
	int s = line * LCD_N5110_WIDTH + start;
	int e = line * LCD_N5110_WIDTH + end;

	lcd_n5110_base_addr(s);

	for (cnt = s; cnt < e; cnt++)
		lcd_n5110_send(0, LCD_N5110_DATA);
}

void lcd_n5110_clear_line(uint8_t line)
{
	lcd_n5110_clear_area(line, 0, LCD_N5110_WIDTH);
}

void lcd_n5110_goto_xy(uint8_t x, uint8_t y)
{
	lcd_n5110_index = x * LCD_N5110_CWIDTH + y * LCD_N5110_WIDTH;
}

void lcd_n5110_goto_xy_exact(uint8_t x, uint8_t y)
{
	lcd_n5110_index = x + y * LCD_N5110_WIDTH;
}

void lcd_n5110_char(char c)
{
	int cnt;
	if (c == '\n') {
		lcd_n5110_index = lcd_n5110_index + LCD_N5110_WIDTH - lcd_n5110_index % LCD_N5110_WIDTH;
	} else {
		lcd_n5110_base_addr(lcd_n5110_index);
		for (cnt = 0; cnt < (LCD_N5110_CWIDTH - 1); cnt++)
			lcd_n5110_send(pgm_read_byte(&font5x7[c - 32][cnt]) << 1, LCD_N5110_DATA);
		lcd_n5110_send(0, LCD_N5110_DATA);
		lcd_n5110_index += LCD_N5110_CWIDTH;
	}
}

void lcd_n5110_string(char *s)
{
	while (*s)
		lcd_n5110_char(*s++);
}

#define ASIZE 256
void lcd_n5110_format_string(char *s, ...)
{
	char ns[ASIZE];
	va_list args;
	va_start(args, s);
	vsnprintf(ns, ASIZE - 1, s, args);
	va_end(args);

	lcd_n5110_string(ns);
}

void lcd_n5110_send(uint8_t d, uint8_t cmd)
{
	int cnt = 0;
	LCD_N5110_DDR |= (1 << LCD_N5110_DIN);
	LCD_N5110_DDR |= (1 << LCD_N5110_DC);

	LCD_N5110_PORT &= ~(1 << LCD_N5110_CE);
	if (cmd == LCD_N5110_DATA)
		LCD_N5110_PORT |= (1 << LCD_N5110_DC);
	else
		LCD_N5110_PORT &= ~(1 << LCD_N5110_DC);

	for (cnt = 0; cnt < 8; cnt++) {
		if ((d >> (7 - cnt)) & 0x01)
			LCD_N5110_PORT |= (1 << LCD_N5110_DIN);
		else
			LCD_N5110_PORT &= ~(1 << LCD_N5110_DIN);

		LCD_N5110_PORT |= (1 << LCD_N5110_CLK);
		LCD_N5110_PORT &= ~(1 << LCD_N5110_CLK);
	}
	LCD_N5110_PORT |= (1 << LCD_N5110_CE);

	LCD_N5110_DDR &= ~(1 << LCD_N5110_DIN);
	LCD_N5110_DDR &= ~(1 << LCD_N5110_DC);

	LCD_N5110_PORT |= (1 << LCD_N5110_DIN);
	LCD_N5110_PORT |= (1 << LCD_N5110_DC);
}

void lcd_n5110_base_addr(uint16_t addr)
{
	lcd_n5110_send((0x80 | (addr % LCD_N5110_WIDTH)), LCD_N5110_CMD);
	lcd_n5110_send((0x40 | (addr / LCD_N5110_WIDTH)), LCD_N5110_CMD);
}

void lcd_n5110_drawpixel(uint8_t x, uint8_t y, int c)
{
	int pixel = x + y / LCD_N5110_CHEIGT * LCD_N5110_WIDTH;

	lcd_n5110_send((0x80 | (pixel % LCD_N5110_WIDTH)), LCD_N5110_CMD);
	lcd_n5110_send((0x40 | (pixel / LCD_N5110_WIDTH)), LCD_N5110_CMD);

	if (c)
		lcd_n5110_send((1 << (y % LCD_N5110_CHEIGT)), LCD_N5110_DATA);
	else
		lcd_n5110_send(0, LCD_N5110_DATA);
}

void lcd_n5110_drawimage(const unsigned char *data)
{
	int cnt = 0;
	int pixel = LCD_N5110_WIDTH * (LCD_N5110_HEIGHT / LCD_N5110_CHEIGT);
	lcd_n5110_goto_xy(0 , 0);

	for (cnt = 0; cnt < pixel; cnt++) {
		lcd_n5110_base_addr(lcd_n5110_index);
		lcd_n5110_send(pgm_read_byte(data++), LCD_N5110_DATA);
		lcd_n5110_index++;
	}
}
